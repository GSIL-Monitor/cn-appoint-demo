public class FinishButton {
    /**
     * 完成的按钮
     */
    private String buttonName;
    /**
     * 链接
     */
    private String url;

    public String getButtonName() {
        return buttonName;
    }

    public void setButtonName(String buttonName) {
        this.buttonName = buttonName;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }
}
